class CarMark {
  final int id;
  final String name;
  final String cyrillic_name;
  final int year_from;
  final int year_to;
  final String country;
  final String logo;

  CarMark({
    required this.id,
    required this.name,
    required this.cyrillic_name,
    required this.year_from,
    required this.year_to,
    required this.country,
    required this.logo,
  });
}
